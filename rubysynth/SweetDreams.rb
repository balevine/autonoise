    bassTrack.notes << Note.new("A", 2, 16)
    bassTrack.notes << Note.new("", 1, 16)
  }
  
  voiceTrack = Track.new(voice)
  4.times {
    voiceTrack.notes << Note.new("", 1, 1)
  }
  voiceTrack.notes << Note.new("", 1, 4)
  voiceTrack.notes << Note.new("C", 4, 8)
  voiceTrack.notes << Note.new("C", 4, 16)
  voiceTrack.notes << Note.new("", 4, 16)
  voiceTrack.notes << Note.new("C", 4, 8)
  voiceTrack.notes << Note.new("C", 4, 16)
  voiceTrack.notes << Note.new("", 4, 16)
  voiceTrack.notes << Note.new("A", 4, 4)
  voiceTrack.notes << Note.new("C", 4, 16)
  voiceTrack.notes << Note.new("C", 4, 32)
  voiceTrack.notes << Note.new("", 4, 32)
  voiceTrack.notes << Note.new("C", 4, 8)
  voiceTrack.notes << Note.new("C", 4, 16)
  voiceTrack.notes << Note.new("", 4, 16)
  voiceTrack.notes << Note.new("C", 4, 8)
  voiceTrack.notes << Note.new("B", 4, 2)
  
  voiceTrack.notes << Note.new("C", 4, 16)
