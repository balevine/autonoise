  voiceTrack.notes << Note.new("C", 4, 32)
  voiceTrack.notes << Note.new("", 4, 32)
  voiceTrack.notes << Note.new("C", 4, 8)
  voiceTrack.notes << Note.new("D", 4, 8)
  voiceTrack.notes << Note.new("C", 4, 4)
  voiceTrack.notes << Note.new("B", 4, 4)

  
  s = Song.new()
  s.tracks = [drumTrack, bassTrack, voiceTrack]
  
  return s.nextSamples(s.sampleLength)
end