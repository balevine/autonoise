  24.times {
    drumTrack.notes << Note.new("", 0, 4)
    drumTrack.notes << Note.new("A", 0, 16)
    drumTrack.notes << Note.new("", 0, 16)
    drumTrack.notes << Note.new("", 0, 8)
  }
  
  bassTrack = Track.new(synthBass)
  6.times {
    bassTrack.notes << Note.new("A", 0, 16)
